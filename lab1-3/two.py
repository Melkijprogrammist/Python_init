# Дана строка, состоящая из русских слов, набранных заглавными буквами и разделенных пробелами (одним или несколькими). Найти количество слов, которые начинаются и заканчиваются одной и той же буквой.
import re

def count_same_letter_words(text):
    """
    Подсчитывает количество слов в строке, которые начинаются и заканчиваются одной и той же буквой.

    Args:
        text: Строка, состоящая из русских слов, набранных заглавными буквами и разделенных пробелами.

    Returns:
        Количество слов, которые начинаются и заканчиваются одной и той же буквой.
    """
    words = re.findall(r'\b[А-Я]+\b', text) # Извлекаем слова, состоящие из заглавных русских букв
    count = 0
    for word in words:
        if len(word) > 0 and word[0] == word[-1]: # or if word and word[0] == word[-1]
            count += 1
    return count


# Примеры использования:
text = "СОСНА ДОМ ААА МАМА КОТ"
count = count_same_letter_words(text)
print(f"Количество слов, начинающихся и заканчивающихся на одну букву: {count}") # Вывод: 3

text = "   " # Только пробелы
count = count_same_letter_words(text)
print(f"Количество слов: {count}") # 0

text = "" # Пустая строка
count = count_same_letter_words(text)
print(f"Количество слов: {count}") # 0




import re

def count_words(text):
    """Подсчитывает количество слов в строке, состоящей из русских слов, разделенных пробелами.

    Args:
        text: Строка с русскими словами.

    Returns:
        Количество слов в строке.
        Возвращает 0, если строка пустая или состоит только из пробелов.
    """


    # Вариант 1: Использование split() (более простой, но может быть менее надежный при большом количестве пробелов)
    words = text.split()
    return len(words)


# Вариант 2: Использование регулярных выражений (более надежный):

def count_words_regex(text):
    """Подсчитывает количество слов в строке с помощью регулярных выражений.

    Args:
        text: Строка с русскими словами.

    Returns:
        Количество слов в строке.
        Возвращает 0, если строка пустая или состоит только из пробелов.
    """
    words = re.findall(r'\b\w+\b', text) # Находит все слова, состоящие из букв, цифр и подчеркиваний
    return len(words)




# Примеры использования:

text = "Привет мир это   тест строка"
word_count = count_words(text)
print(f"Количество слов (split): {word_count}") # Вывод: 5

word_count_regex = count_words_regex(text)
print(f"Количество слов (regex): {word_count_regex}") # Вывод: 5


text = "   " # Только пробелы
word_count = count_words(text)
print(f"Количество слов (split): {word_count}") # Вывод: 0

text = "" # Пустая строка
word_count = count_words(text)
print(f"Количество слов (split): {word_count}") # Вывод: 0
